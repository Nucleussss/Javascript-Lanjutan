//          javascript Asyncronous

//      Single dan Multi Thread
// Single Theard bisa diibaratkan sebagai 1 pekerja yang mengerjakan semua task sendirian.
// jika Multi Theard bisa ibaratkan sebagai 1 kelompok yang mengerjakan semua task.

//      Blocking dan Non-Blocking
// Blocking  = theard mengerjakan task secara urut.
// Non-Blocking = Theard bisa saja mengerjakan task dengan bergantian.

//      Syncronous dan Asyncronous
// Syncronous mengerjakan task secara urut.
// Asyncronous memungkingkan task dikerjakan secara tidak urut. 

// Asyncronous + Single-Threaded = Concurrency.
// Asyncronous + Multi-Threaded = Parallelism.

//      Kesimpulan
// Single dan Multi Thread -> adalah lingkup eksekusi 'task'
// Blocking dan Non-Blocking -> berhubungan dengan Teknik 'ngoding' dalam bidang IO(Inpuit,Output)
// Syncronous dan Asyncronous -> berhubungan dengan Teknik 'ngoding' dalam bidang (HTTP request)
// Concurrency dan Parallelism -> adalah lingkup eksekusi 'task'